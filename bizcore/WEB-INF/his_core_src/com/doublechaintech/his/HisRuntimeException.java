
package com.doublechaintech.his;


public class HisRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public HisRuntimeException() {
        super();
    }


    public HisRuntimeException(String message) {
        super(message);
    }


    public HisRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public HisRuntimeException(Throwable cause) {
        super(cause);
    }


   
}









